function getArrIndex(arr, name) {
	const i = arr.findIndex(v => v.name === name);
	if (i || i === 0)
		// return arr[i];
		return i;
	else {
		console.error(`(!)-USER'S `, `invalid name ${name} in`, arr);
		throw new Error(`invalid name ${name}`)
	}
}
